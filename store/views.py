def updateItem(request):
    """Update the quantity of an item in the cart.

    Accepts a POST request with JSON data containing the product ID and the action (add/remove).
    Updates the quantity of the corresponding order item and deletes the item if the quantity is zero.

    Returns:
        JsonResponse: JSON response indicating the status of the item update.

    """

    data = json.loads(request.body)
    productId = data['productId']
    action = data['action']
    print('Action:', action)
    print('Product:', productId)

    customer = request.user.customer
    product = Product.objects.get(id=productId)
    order, created = Order.objects.get_or_create(customer=customer, complete=False)

    orderItem, created = OrderItem.objects.get_or_create(order=order, product=product)

    if action == 'add':
        orderItem.quantity = (orderItem.quantity + 1)
    elif action == 'remove':
        orderItem.quantity = (orderItem.quantity - 1)

    orderItem.save()

    if orderItem.quantity <= 0:
        orderItem.delete()

    return JsonResponse('Item was added', safe=False)
